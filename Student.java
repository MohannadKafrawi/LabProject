
public class Student {

	private String name;
	private int password;
	
	public Student(String name, int password)
	{
		this.name = name;
		this.password = password;
		
	}
	
	public String getName()
	{
		return name;
	}
	
	public int getPass()
	{
		return password;
	}
	

	

	
}
